
#1. Пользователь вводит в консоли число. Если оно больше 10, выдаем в консоли сообщение “Число больше десяти”, если меньше – “Число меньше десяти”
def task_1():
    number = int(input('Введите число:\n'))
    if number > 10:
        print('Число больше десяти')
    elif number == 10:
        print('Число равно 10')
    else:
        print('Число меньше десяти')
    return 0

