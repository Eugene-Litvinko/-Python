#8. Программа “Магазин”, выводим меню из трех товаров с ценами. Пользователь выбирает товар, вводит сумму денег. Если сумма меньше цены, выводим сообщение: “Денег не хватает!”, если сумма равна цене – “Спасибо за покупку!”, если больше – “Ваша сдача: [сумма сдачи]”
def task_8():
    catalog = {'Молоко': 5, 'Кефир': 4, 'Йогурт': 6}
    x = input('Choose goods: ')
    y = int(input('Amount cash: '))
    if y > catalog.get(x):
        print(f'Ваша сдача: {y - catalog.get(x)}')
    elif y == catalog.get(x):
        print('Спасибо за покупку')
    else:
        print('Денег не хватает!')
    return 0

